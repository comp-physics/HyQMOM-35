function C4 = S4toC4_3D(C200,C110,C101,C020,C011,C002,S300,S210,S201,S120,S111,S102,S030,S021,S012,S003,...
                        S400,S310,S301,S220,S211,S202,S130,S121,S112,S103,S040,S031,S022,S013,S004)
%S4toC4_3D - 35 central moments from standardized moments
%
C4 = zeros(5,5,5);
%
sC200 = sqrt(C200);
sC020 = sqrt(C020);
sC002 = sqrt(C002);
%
C4(1,1,1) = 1;
%
C4(2,1,1) = 0;
C4(1,2,1) = 0;
C4(1,1,2) = 0;
%    
C4(3,1,1)=C200;
C4(2,2,1)=C110;
C4(2,1,2)=C101;
C4(1,3,1)=C020;
C4(1,2,2)=C011;
C4(1,1,3)=C002;
%
C4(4,1,1)=S300*sC200^3;
C4(3,2,1)=S210*sC200^2*sC020;
C4(3,1,2)=S201*sC200^2*sC002;
C4(2,3,1)=S120*sC200*sC020^2;
C4(2,2,2)=S111*sC200*sC020*sC002;
C4(2,1,3)=S102*sC200*sC002^2;
C4(1,4,1)=S030*sC020^3;
C4(1,3,2)=S021*sC020^2*sC002;
C4(1,2,3)=S012*sC020*sC002^2;
C4(1,1,4)=S003*sC002^3;
%
C4(5,1,1)=S400*sC200^4;
C4(4,2,1)=S310*sC200^3*sC020;
C4(4,1,2)=S301*sC200^3*sC002;
C4(3,3,1)=S220*sC200^2*sC020^2;
C4(3,2,2)=S211*sC200^2*sC020*sC002;
C4(3,1,3)=S202*sC200^2*sC002^2;
C4(2,4,1)=S130*sC200*sC020^3;
C4(2,3,2)=S121*sC200*sC020^2*sC002;
C4(2,2,3)=S112*sC200*sC020*sC002^2;
C4(2,1,4)=S103*sC200*sC002^3;
C4(1,5,1)=S040*sC020^4;
C4(1,4,2)=S031*sC020^3*sC002;
C4(1,3,3)=S022*sC020^2*sC002^2;
C4(1,2,4)=S013*sC020*sC002^3;
C4(1,1,5)=S004*sC002^4;
%
end
